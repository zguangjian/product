 * @method static \Illuminate\Database\Eloquent\Builder|\App\Models\MenuModel whereIcons($value)
 */
class MenuModel extends BaseModel
{
    protected $fillable = ['parent_id', 'name', 'url', 'icons', 'sort', 'status', 'content'];

    public static function boot()
    {
        parent::boot(); // TODO: Change the autogenerated stub

    }

    /**
     * 关联permissions表
     * @return \Illuminate\Database\Eloquent\Relations\HasOne
     */
    public function permissions()
    {
        return $this->hasOne(PermissionsModel::class, 'mid', 'id');
    }

    /**
     * 获取父级菜单
     * @param int $parent_id
     * @return array
     */
    public static function parentMenu($parent_id = 0)
    {
        $menuTableList = MenuModel::orderBy("sort")->get()->toArray();
        return getTreeArray($menuTableList, $parent_id, 2);
    }
}
