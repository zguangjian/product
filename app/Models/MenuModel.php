 * @property string|null $content 描述
 * @property \Illuminate\Support\Carbon|null $created_at
 * @property \Illuminate\Support\Carbon|null $updated_at
 * @method static \Illuminate\Database\Eloquent\Builder|\App\Models\MenuModel newModelQuery()
 * @method static \Illuminate\Database\Eloquent\Builder|\App\Models\MenuModel newQuery()
 * @method static \Illuminate\Database\Eloquent\Builder|\App\Models\MenuModel query()
 * @method static \Illuminate\Database\Eloquent\Builder|\App\Models\MenuModel whereContent($value)
